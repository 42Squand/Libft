#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	if (!src && !dest)
		return (NULL);
	i = -1;
	while (++i < n)
		((char *)dest)[i] = ((const char *)src)[i];
	return (dest);
}
